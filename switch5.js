var a,b 
a = Number(prompt("Введите число а"))
b = Number(prompt("Введите число b"))
b != 0
x = prompt("Введите номер действия")
switch (x){
    case "1" : alert(a + b);
        break
    case "2" : alert(a - b);
        break
    case "3" : alert(a * b);
        break
    case "4" : alert(a / b);
        break}